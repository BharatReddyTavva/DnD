
CREATE PROCEDURE [dbo].[usp_GetCountryList]
/*****************************************************************************************************************
FUNCTION:	[usp_GetCountryList]
PURPOSE:	Get all countries.
NOTES:  	
CREATED:	
REVISIONS:
Date		   Author			Description

*****************************************************************************************************************/
AS
BEGIN
	SELECT CountryId,Name FROM [dbo].[Country] Where IsActive = 1
	

END
